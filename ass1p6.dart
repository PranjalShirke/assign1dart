  void main(){  
    var number=4;
    if (number >= 0 && number <= 5) {
      // Print the spelling of the number
      if (number == 0) {
        print("Zero");
      } else if (number == 1) {
        print("One");
      } else if (number == 2) {
        print("Two");
      } else if (number == 3) {
        print("Three");
      } else if (number == 4) {
        print("Four");
      } else {
        print("Five");
      }
    } else {
      // Print if the number is greater than 5
      print("Entered number is greater than 5.");
    }
  } 
    